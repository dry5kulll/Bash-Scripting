#!/bin/bash

# Declare variables and assign values
num1=10
num2=2


# Perform arithmetic operations
addition=$((num1+num2))
subtraction=$((num1-num2))
product=$((num1*num2))
division=$((num1/num2))
modulus=$((num1%num2))


# Display results
echo Addition: $addition
echo Subtraction: $subtraction
echo Product: $product
echo Division: $division
echo Modulus: $modulus
